def build_render_job(thumb: Path, scene_clips: list, audio: Path, duration: float,
                     out_w: int, out_h: int, codec_info: dict):
    """Build remote render job (server uses {input_N} placeholders)."""
    # Inputs: thumb(0), clip_0(1)..clip_N(N), audio(N+1)
    all_uploads = [thumb] + scene_clips + [audio]
    n_clips = len(scene_clips)

    cmd_inputs = ["-r", str(codec_info["fps"]), "-loop", "1", "-i", "{input_0}"]
    for i in range(n_clips):
        cmd_inputs += ["-stream_loop", "-1", "-i", f"{{input_{i+1}}}"]
    cmd_inputs += ["-i", f"{{input_{n_clips+1}}}"]

    fc = _build_filter_remote(thumb, scene_clips, codec_info["fps"], out_w, out_h, duration)
    # Replace absolute Path objects in filter with placeholders (paths are not embedded in filter)

    cmd = ["-y", "-filter_threads", "8"]
    cmd += cmd_inputs
    cmd += ["-filter_complex", fc, "-map", "[outv]", "-map", "[aout]"]
    cmd += [
        "-c:v", codec_info["cv"],
        "-preset", codec_info.get("preset", "slow"),
        "-b:v", codec_info["bv"],
    ]
    if codec_info.get("profile"):
        cmd += ["-profile:v", codec_info["profile"]]
    if codec_info.get("level"):
        cmd += ["-level:v", codec_info["level"]]
    cmd += [
        "-c:a", "aac",
        "-b:a", codec_info["ba"],
        "-ar", "48000",
        "-t", str(duration),
        "-movflags", "+faststart",
        "{output_0}",
    ]
    return cmd, all_uploads


def build_local_render_job(thumb: Path, scene_clips: list, audio: Path, duration: float,
                            out_w: int, out_h: int, codec_info: dict, output_path: Path) -> list:
    """Build local ffmpeg command (VideoToolbox)."""
    n_clips = len(scene_clips)

    cmd_inputs = ["-r", str(codec_info["fps"]), "-loop", "1", "-i", str(thumb)]
    for clip in scene_clips:
        cmd_inputs += ["-stream_loop", "-1", "-i", str(clip)]
    cmd_inputs += ["-i", str(audio)]

    fc = _build_filter_local(thumb, scene_clips, codec_info["fps"], out_w, out_h, duration)

    cmd = ["ffmpeg", "-y", "-filter_threads", "4"]
    cmd += cmd_inputs
    cmd += ["-filter_complex", fc, "-map", "[outv]", "-map", "[aout]"]
    cmd += [
        "-c:v", codec_info["cv"],
        "-b:v", codec_info["bv"],
        "-q:v", str(codec_info["q_v"]),
    ]
    if codec_info.get("allow_sw"):
        cmd += ["-allow_sw", "1"]
    cmd += [
        "-pix_fmt", codec_info["pix_fmt"],
        "-c:a", "aac",
        "-b:a", codec_info["ba"],
        "-ar", "48000",
        "-t", str(duration),
        "-movflags", "+faststart",
        str(output_path),
    ]
    return cmd


