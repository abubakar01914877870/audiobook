#!/usr/bin/env python3
"""
render_video_videos.py — Export YouTube + TikTok chapter videos from Grok scene MP4 clips.

Stitches scene MP4 clips (generated by generate_video.py) in sequence with the
chapter narration audio. A static thumbnail image is used as a 5s intro card.

For each chapter folder the script expects:
  • *_NN_thumb.(png|jpg|webp)  — static intro card (first 5s)
  • *_NN_scene.mp4              — Grok video clips (stitched in order)
  • narration audio (.mp3/.m4a/.aac/.wav/.flac)

Output:
  • <stem>_youtube.mp4  — 1440×2560 (2K), av1_qsv / hevc_videotoolbox, 24 fps
  • <stem>_tiktok.mp4   — 1080×1920,       h264_qsv / h264_videotoolbox, 30 fps

Usage:
    python render_video_videos.py /path/to/folder

─────────────────────────────────────────────────────────────────────────────
Intel Arc A750 QSV — Pipeline Rules (DO NOT CHANGE without reading this)
─────────────────────────────────────────────────────────────────────────────
(See render_video_images.py header for full QSV rules)

Key notes for video concat pipeline:
  - Each scene MP4 clip is loop-trimmed to fill the audio (using -stream_loop -1
    on the remote path and a concat+trim approach locally).
  - No xfade transitions — clips are stitched sequentially (cut-to-cut).
  - Ambient audio from scene clips is mixed at 7% volume under narration.
─────────────────────────────────────────────────────────────────────────────
"""

