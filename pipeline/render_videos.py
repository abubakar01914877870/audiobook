def get_audio_duration(audio_path: Path) -> float:
    try:
        r = subprocess.run(
            ["ffprobe", "-v", "quiet", "-print_format", "json", "-show_format", str(audio_path)],
            capture_output=True, text=True, check=True
        )
        return float(json.loads(r.stdout)["format"]["duration"])
    except Exception:
        r = subprocess.run(
            ["ffprobe", "-v", "quiet", "-print_format", "json", "-show_streams", str(audio_path)],
            capture_output=True, text=True, check=True
        )
        data = json.loads(r.stdout)
        for s in data.get("streams", []):
            if "duration" in s:
                return float(s["duration"])
    raise ValueError(f"Could not read audio duration for {audio_path.name}")


def find_thumb_image(folder: Path):
    """Find the _NN_thumb image file (PNG/JPG/WebP)."""
    for f in sorted(folder.iterdir()):
        if not f.is_file():
            continue
        if re.search(r'_\d+_thumb\.(png|jpg|jpeg|webp)$', f.name.lower()):
            return f
    return None


def find_scene_mp4s(folder: Path) -> list:
    """Find all _NN_scene.mp4 files sorted by NN."""
    clips = []
    for f in sorted(folder.iterdir()):
        if not f.is_file():
            continue
        m = re.search(r'_(\d+)_scene\.mp4$', f.name.lower())
        if m:
            clips.append((int(m.group(1)), f))
    clips.sort(key=lambda x: x[0])
    return [f for _, f in clips]


def human_size(p: Path):
    s = p.stat().st_size
    for u in ["B", "KB", "MB", "GB"]:
        if s < 1024:
            return f"{s:.1f} {u}"
        s /= 1024
    return f"{s:.1f} TB"


def check_server_with_retries(n: int = 3) -> bool:
    for attempt in range(1, n + 1):
        print(f"  [attempt {attempt}/{n}] Contacting render server...")
        try:
            resp = requests.get(f"{RENDER_SERVER_URL}/health", timeout=3)
            if resp.headers is not None:
                print("  Render server online.")
                return True
        except Exception:
            pass
        print(f"  Attempt {attempt} failed.")
        if attempt < n:
            time.sleep(2)
    print(f"  Render server unreachable after {n} attempts.")
    return False


# ─────────────────────────────────────────────────────────────────────────────
# Build FFmpeg command
# ─────────────────────────────────────────────────────────────────────────────
#
# Layout:
#   [0]     : thumb image → static 5s intro at output resolution
#   [1..N]  : scene MP4 clips, each looped to fill its segment
#
# The clips are divided equally across (duration - 5s). Each clip is looped
# and trimmed to its allocated time. Cuts are sharp (no xfade — stitched in
# sequence per user request).
#
# Audio:
#   Each video clip contributes ambient audio at AMBIENT_VOL (7%), crossfaded
#   at segment boundaries and mixed under the narration track.

def _build_filter_remote(thumb_path: Path, scene_clips: list, fps: int,
                          out_w: int, out_h: int, duration: float):
    """Build -filter_complex and -i args for remote (QSV) render."""
    THUMB_DUR = 5.0
    n_clips = len(scene_clips)
    if n_clips == 0:
        raise ValueError("No scene clips to render.")

    clip_dur = (duration - THUMB_DUR) / n_clips   # seconds per clip

    # Input list: [thumb_img] + [clip_0, clip_1, ..., clip_N-1] + [audio]
    # thumb is index 0 (static image, looped at fps)
    # clips are indices 1..N (video, stream-looped)
    # audio is index N+1

    filter_parts = []

    # Scale thumb → output resolution
    filter_parts.append(
        f"[0:v]"
        f"scale={out_w}:{out_h}:force_original_aspect_ratio=increase:flags=lanczos,"
        f"crop={out_w}:{out_h},setsar=1,fps={fps},"
        f"trim=duration={THUMB_DUR:.3f},"
        f"format=yuv420p[vthumb]"
    )

    # Scale and trim each scene clip
    for i, _ in enumerate(scene_clips):
        inp = i + 1   # input index (0=thumb, 1..N=clips)
        filter_parts.append(
            f"[{inp}:v]"
            f"scale={out_w}:{out_h}:force_original_aspect_ratio=increase:flags=lanczos,"
            f"crop={out_w}:{out_h},setsar=1,fps={fps},"
            f"trim=duration={clip_dur:.3f},setpts=PTS-STARTPTS,"
            f"format=yuv420p[vclip{i}]"
        )

    # Concatenate all video segments
    all_segs = "[vthumb]" + "".join(f"[vclip{i}]" for i in range(n_clips))
    n_total = 1 + n_clips
    filter_parts.append(
        f"{all_segs}concat=n={n_total}:v=1:a=0,"
        f"format=nv12[outv]"
    )

    # Ambient audio: trim each clip's audio to its segment duration, then concat
    for i, _ in enumerate(scene_clips):
        inp = i + 1
        filter_parts.append(
            f"[{inp}:a]atrim=duration={clip_dur:.3f},asetpts=PTS-STARTPTS[va{i}]"
        )

    # Silence for thumb intro duration
    filter_parts.append(
        f"anullsrc=r=44100:cl=stereo,"
        f"atrim=duration={THUMB_DUR:.3f},"
        f"asetpts=PTS-STARTPTS[vathumb]"
    )

    # Concat all audio segments (thumb silence + clip ambients)
    audio_segs = "[vathumb]" + "".join(f"[va{i}]" for i in range(n_clips))
    filter_parts.append(
        f"{audio_segs}concat=n={n_total}:v=0:a=1[ambraw]"
    )

    # Mix ambient under narration
    narration_idx = n_clips + 1  # input index
    fade_out_st = max(0.0, duration - 3.0)
    filter_parts.append(f"[ambraw]volume={AMBIENT_VOL}[ambient]")
    filter_parts.append(
        f"[{narration_idx}:a][ambient]"
        f"amix=inputs=2:normalize=0:duration=first:dropout_transition=0[mixed]"
    )
    filter_parts.append(
        f"[mixed]"
        f"afade=t=in:st=0:d=2,"
        f"afade=t=out:st={fade_out_st:.3f}:d=3[aout]"
    )

    return ";".join(filter_parts)


